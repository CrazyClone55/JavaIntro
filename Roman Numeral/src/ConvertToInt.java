public class ConvertToInt {
    int convert(String str){
        if (str == "I") {
            return 1;
        } else if (str == "II") {
            return 2;
        } else if (str == "III") {
            return 3;
        } else if (str == "IV") {
            return 4;
        } else if (str == "V") {
            return 5;
        } else if (str == "VI") {
            return 6;
        } else if (str == "VII") {
            return 7;
        } else if (str == "VIII") {
            return 8;
        } else if (str == "IX") {
            return 9;
        } else if (str == "X") {
            return 10;
        } else {
            return -1;
        }
    }
}
